package co.diginex.demoplayer.ui;

import android.support.test.espresso.UiController;
import android.support.test.espresso.ViewAction;
import android.view.View;

import org.hamcrest.Matcher;

import co.diginex.demoplayer.R;

/**
 * Created by Sean on 14/05/2016.
 */
public class RecyclerItemClickAction {

    public static ViewAction clickItemImageViewWithId(final int id) {
        return new ViewAction() {
            @Override
            public Matcher<View> getConstraints() {
                return null;
            }

            @Override
            public String getDescription() {
                return "Click on the image in the recycler view for the given id";
            }

            @Override
            public void perform(UiController uiController, View view) {
                View v = view.findViewById(id);
                if (v != null) {
                    v.performClick();
                }
            }
        };
    }
}
